/**
 * The `TypeOfService` enum defines the various types of medical services
 * that can be provided in the hospital management system.
 */
public enum TypeOfService {
    /**
     * General consultation with a doctor.
     */
    CONSULTATION,

    /**
     * Laboratory testing services.
     */
    LAB_SERVICES,

    /**
     * Radiological services, including X-rays, MRIs, and CT scans.
     */
    RADIOLOGY,

    /**
     * Emergency medical services.
     */
    EMERGENCY_SERVICES,

    /**
     * Vaccination services.
     */
    VACCINATION,

    /**
     * Follow-up appointments after initial consultation or treatment.
     */
    FOLLOW_UP,

    /**
     * Comprehensive health check-up services.
     */
    HEALTH_CHECKUP
}

/**
 * The `UserRole` enum defines the different roles a user can have
 * in the hospital management system.
 */
public enum UserRole {
    /**
     * Represents a doctor role.
     */
    Doctor,

    /**
     * Represents a patient role.
     */
    Patient,

    /**
     * Represents an administrator role.
     */
    Administrator,

    /**
     * Represents a pharmacist role.
     */
    Pharmacist
}

/**
 * The `PrescriptionStatus` enum represents the status of a prescription in a healthcare management system.
 * It includes the following statuses:
 * 
 *     `PENDING`: The prescription is awaiting fulfillment.
 *     `DISPENSED`: The prescription has been fulfilled and dispensed to the patient.
 *     `CANCELLED`: The prescription has been cancelled and is no longer valid.
 * 
 */
public enum PrescriptionStatus {
    PENDING,
    DISPENSED,
    CANCELLED
}
